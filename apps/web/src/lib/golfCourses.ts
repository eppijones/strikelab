// Combined courses list
export const ALL_COURSES: GolfCourse[] = [...NORWEGIAN_COURSES, ...INTERNATIONAL_COURSES]

// Helper functions
export function getCoursesByCountry(countryCode: string): GolfCourse[] {
  return ALL_COURSES.filter(c => c.countryCode === countryCode)
}

export function getCoursesByType(type: GolfCourse['type']): GolfCourse[] {
  return ALL_COURSES.filter(c => c.type === type)
}

export function getCoursesByPriceRange(range: GolfCourse['priceRange']): GolfCourse[] {
  return ALL_COURSES.filter(c => c.priceRange === range)
}

export function searchCourses(query: string): GolfCourse[] {
  const q = query.toLowerCase()
  return ALL_COURSES.filter(c => 
    c.name.toLowerCase().includes(q) ||
    c.city.toLowerCase().includes(q) ||
    c.country.toLowerCase().includes(q) ||
    (c.region?.toLowerCase().includes(q))
  )
}

export function getCourseById(id: string): GolfCourse | undefined {
  return ALL_COURSES.find(c => c.id === id)
}

