// Golf Courses Database
export interface GolfCourse {
  id: string
  name: string
  city: string
  region?: string
  country: string
  countryCode: string
  par: number
  holes: number
  courseRating?: number
  slopeRating?: number
  length?: number // in meters
  lengthYards?: number
  established?: number
  designer?: string
  type: 'links' | 'parkland' | 'heathland' | 'desert' | 'mountain' | 'resort'
  facilities: string[]
  website?: string
  phone?: string
  latitude?: number
  longitude?: number
  priceRange: 'budget' | 'moderate' | 'premium' | 'luxury'
  imageUrl?: string
  description?: string
  holeData?: HoleData[]
}

export interface HoleData {
  number: number
  par: number
  handicap: number
  whiteTee?: number // meters
  yellowTee?: number
  blueTee?: number
  redTee?: number
}

